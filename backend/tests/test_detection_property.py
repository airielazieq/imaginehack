"""Property-based tests for detection output (task 3.6).

Exercises three correctness properties of the Module 1 detection pipeline
(`backend/modules/detection_insight/detector.py`) using Hypothesis. Telemetry is
generated by varying values *around the detection-rule thresholds* (see
`backend/rules/detection_rules.json`) and pairing each payload with a workload
context whose environment / construction_workflow satisfies the targeted rule,
so every generated example is guaranteed to make at least one detection rule
fire and therefore produce an Issue.

**Property 2: Detection Output Structure Invariant**
    For any telemetry that produces an Issue, the Issue carries a non-null
    ``ml_result`` (numeric anomaly_score + boolean is_anomaly), an
    ``xai_explanation`` with >= 3 ``top_contributing_factors`` each referencing
    a feature from the input feature set, and a non-empty
    ``llm_user_explanation``.

**Property 3: Issue Classification Invariant**
    For any produced Issue, ``issue_type`` is one of the 7 defined types,
    ``severity`` is one of {low, medium, high, critical}, and
    ``confidence_score`` is in [0.0, 1.0].

**Property 4: Issue Consolidation**
    For multiple detections on the same workload within the 5-minute window, the
    detector consolidates to a single Issue whose severity equals the maximum of
    the contributing detections' severities (no duplicate issues).

**Validates: Requirements 2.1, 2.2, 3.1, 3.2, 3.3, 4.1, 4.2**

An isolated temp SQLite DB is configured via ``CLOVER_DB_PATH`` before the app /
config is imported so these tests never touch the real ``clover.db``. The
detector's DB-backed helpers are exercised directly via the ``db_path``
argument.
"""

